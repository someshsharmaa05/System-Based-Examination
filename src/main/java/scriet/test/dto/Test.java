package scriet.test.dto;

public class Test {
	private long strol ;
	private int marks;
	
	public Test() {
		super();
	}
	public Test(long strol, int marks) {
		super();
		this.strol = strol;
		this.marks = marks;
	}
	public long getStrol() {
		return strol;
	}
	public void setStrol(long strol) {
		this.strol = strol;
	}
	public int getMarks() {
		return marks;
	}
	public void setMarks(int marks) {
		this.marks = marks;
	}
	
}
